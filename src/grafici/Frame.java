    public Color nextColor() {
        contatoreColore++;
        Color[] colori = {Color.BLUE, Color.GREEN, Color.RED, Color.YELLOW, Color.LIGHT_GRAY, Color.CYAN, Color.MAGENTA, Color.ORANGE, Color.PINK};
        if (contatoreColore == 9) {
            contatoreColore = 0;
        }
        return colori[contatoreColore];
    }
    public void draw(Punto punto) {
        
        if (!(punto.equals(lastPunto))) {
            g2.drawOval(punto.getCanvasX(), punto.getCanvasY(), 1, 1);
            lastPunto = punto;
        }
        //g2.drawOval(punto.getCanvasX(), punto.getCanvasY(), 1, 1);
    }
    public void drawPiano() {
        g2.setColor(Color.black);
        g2.drawLine(canvas.getWidth()/2, 0,canvas.getWidth()/2, canvas.getHeight());
        g2.drawLine(0, canvas.getHeight()/2,canvas.getWidth(), canvas.getHeight()/2);
        
        g2.drawLine(canvas.getWidth()/2 + 5, + 8, canvas.getWidth()/2, 0);
        g2.drawLine(canvas.getWidth()/2 - 5, + 8, canvas.getWidth()/2, 0);
        
        g2.drawLine(canvas.getWidth(), canvas.getHeight()/2, canvas.getWidth() - 8, canvas.getHeight()/2 + 5);
        g2.drawLine(canvas.getWidth(), canvas.getHeight()/2, canvas.getWidth() - 8, canvas.getHeight()/2 - 5);
    }
    public void init() {
        Graphics g = canvas.getGraphics();
        g2 = (Graphics2D) g;
    }
    public void updateValoriPianoCartesiano() {
        mol = Double.parseDouble(jMoltiplicatore.getText());
        calcoli = Math.pow(3, (((double) jSlider1.getValue()) / 10)) * 300;
        //System.out.println("Calcoli: " + calcoli);
        max = 390/mol;
        step = max/calcoli;
    }
    
    
