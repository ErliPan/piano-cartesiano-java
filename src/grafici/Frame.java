    private void fattoAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fattoAction
        
        g2.setColor(nextColor());
        
        Funzione f = new Funzione(jTextField1.getText());

        for (double i = -max;i < max;i += step) {
            //System.out.println(i);
            draw(new Punto((int) (i * mol), (int) (f.calcFun(i) * mol), canvas));
        }
        
    }//GEN-LAST:event_fattoAction

